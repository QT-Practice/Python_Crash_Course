# passing information to a function using parameter and arguments

#defining a function with parameter
def greet_user_with_parameter(username):
    """Funtion used to invoke Hello message
    """
    print(f"Hello, {username}!")

#calling a function and passing an argument
greet_user_with_parameter('jesse')

# username - parameter. information needed for the function to do its job
# jesse - argument. defined in function call. information. 